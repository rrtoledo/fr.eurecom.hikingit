package MyTracks;

import MyTracks.Maps;
import MyTracks.Comments;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

public class TabsAdapterMyTracks extends FragmentPagerAdapter {
	 
    public TabsAdapterMyTracks(FragmentManager fm) {
        super(fm);
    }
 
    @Override
    public Fragment getItem(int index) {
 
        switch (index) {
        case 0:
            return new Maps();
        case 1:
            return new Comments();
        }
 
        return null;
    }
 
    @Override
    public int getCount() {
        // get item count - equal to number of tabs
        return 2;
    }
 
}
